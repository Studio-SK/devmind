#!/usr/bin/env node
// Regenerates docs/notes-data.js from notes/. Run: node scripts/generate-notes-data.js
// Re-run any time notes/ changes, then commit the regenerated docs/notes-data.js.

const fs = require("fs");
const path = require("path");

const repoRoot = path.resolve(__dirname, "..");
const notesRoot = path.join(repoRoot, "notes");
const outFile = path.join(repoRoot, "docs", "notes-data.js");

function walk(dir) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });

  const dirEntries = entries
    .filter((e) => e.isDirectory())
    .sort((a, b) => a.name.localeCompare(b.name));
  const fileEntries = entries
    .filter((e) => e.isFile() && e.name.endsWith(".md"))
    .sort((a, b) => a.name.localeCompare(b.name));

  const children = [];

  for (const d of dirEntries) {
    const childPath = path.join(dir, d.name);
    const childNode = walk(childPath);
    if (childNode) children.push(childNode);
  }

  for (const f of fileEntries) {
    const fullPath = path.join(dir, f.name);
    const relPath = path.relative(notesRoot, fullPath).split(path.sep).join("/");
    children.push({ name: f.name, type: "file", path: relPath });
  }

  if (children.length === 0) return null;

  return {
    name: path.basename(dir),
    type: "dir",
    children,
  };
}

function collectFiles(node, files) {
  if (node.type === "file") {
    const fullPath = path.join(notesRoot, node.path);
    files[node.path] = fs.readFileSync(fullPath, "utf8");
    return;
  }
  for (const child of node.children) collectFiles(child, files);
}

const root = walk(notesRoot);
const tree = root ? root.children : [];

const files = {};
for (const node of tree) collectFiles(node, files);

const data = { tree, files };

const output =
  "// Auto-generated by scripts/generate-notes-data.js — do not hand-edit.\n" +
  "window.NOTES_DATA = " +
  JSON.stringify(data, null, 2) +
  ";\n";

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, output);

console.log(`Wrote ${path.relative(repoRoot, outFile)} (${Object.keys(files).length} files)`);
